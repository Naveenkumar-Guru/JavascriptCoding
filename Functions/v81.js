//81.Write a function to add two numbers
function add(a, b) {
  console.log(a + b);
  console.log(a - b);
  console.log(a * b);
  console.log(a / b);
  console.log(a || b);
  console.log(a > b);
  console.log(a < b);
  console.log(a === b);
  console.log(a !== b);
  console.log("...", a >= b);
  console.log("...", a <= b);
}
add(10, 20);
